package blinkrr.com.ph;

public class Patient {
    public String id, name, contact, gender, address, birthday, image;

    public Patient(String id, String name, String contact, String gender, String address, String birthday, String image) {
        this.id = id;
        this.name = name;
        this.contact = contact;
        this.gender = gender;
        this.address = address;
        this.birthday = birthday;
        this.image = image;
    }
}
